package exercVet;

public class Vects {
	
	private double soma;
	private double media;
	
	public Vects(double soma, double media) {
		this.soma = soma;
		this.media = media;
	}
	public double getSoma() {
		return soma;
	}
	public void setSoma(double soma) {
		this.soma = soma;
	}
	public double getMedia() {
		return media;
	}
	public void setMedia(double media) {
		this.media = media;
	}
	


}
